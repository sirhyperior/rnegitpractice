# GitHub에 코드를 올리는 방법:
# 1. GitHub에 가입하기  
# 2. 새로운 저장소(repository) 만들기
# 3. 터미널에서 git init 명령어로 저장소 초기화하기
# 4. git add . 명령어로 변경된 파일 추가하기
# 5. git commit -m "커밋 메시지" 명령어로
# 6. git remote add origin <저장소 URL> 명령어로 원격 저장소 연결하기
# 7. git push -u origin main 명령어로 코드 올리기